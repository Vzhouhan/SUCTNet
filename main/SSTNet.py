class Attention(nn.Module):
    def __init__(self, dim, num_heads, bias,window_size=(16,16)):
        super(Attention, self).__init__()
        dim_head=dim//num_heads
        self.num_heads = num_heads
        self.temperature = nn.Parameter(torch.ones(num_heads, 1, 1))
        self.windowsize=window_size
        self.qkv = nn.Conv2d(dim, dim * 3, kernel_size=1, bias=bias)
        self.qkv_dwconv = nn.Conv2d(dim * 3, dim * 3, kernel_size=3, stride=1, padding=1, groups=dim * 3, bias=bias)
        self.project_out = nn.Conv2d(dim, dim, kernel_size=1, bias=bias)
        self.project_out1=nn.Conv2d(dim, dim, kernel_size=1, bias=bias)
        self.scale = dim_head ** -0.5
        self.bn=ZM_bn()
        self.pos_emb2 = nn.Sequential(
            nn.Conv2d(dim, dim, 3, 1, 1, bias=False, groups=dim),
            GELU(),
            nn.Conv2d(dim, dim, 3, 1, 1, bias=False, groups=dim),
        )
        seq_l = window_size[0] * window_size[1]
        self.pos_emb = nn.Parameter(torch.Tensor(1, num_heads, seq_l, seq_l))
        trunc_normal_(self.pos_emb)
    def forward(self, x):
        b, c, h, w = x.shape
        w_size=self.windowsize
        qkv = self.qkv_dwconv(self.qkv(x))
        q, k, v = qkv.chunk(3, dim=1)
        v_inp= v
        q1,k1,v1 = map(lambda t: rearrange(t, 'b (h b0) (w b1) c -> (b h w) (b0 b1) c', b0=w_size[0], b1=w_size[1]),(q.permute(0,2,3,1),k.permute(0, 2, 3, 1),v.permute(0, 2, 3, 1)))
        q1, k1, v1 = map(lambda t: rearrange(t, 'b n (h d) -> b h n d', h=self.num_heads), (q1, k1, v1))
        q1 *= self.scale
        sim = einsum('b h i d, b h j d -> b h i j', q1, k1)
        # print(sim.shape, 'sim')
        sim = sim + self.pos_emb
        attn1 = sim.softmax(dim=-1)
        # print(attn.shape, 'attn')
        out1 = einsum('b h i j, b h j d -> b h i d', attn1, v1)
        out1 = rearrange(out1, 'b h n d -> b n (h d)')
        # print(out.shape, 'out')
        # out1 = self.project_out(out1)
        out1 = rearrange(out1, '(b h w) (b0 b1) c -> b (h b0) (w b1) c', h=h // self.windowsize[0], w=w // self.windowsize[1],
                         b0=self.windowsize[0])
        out1=self.project_out1(out1.permute(0,3,1,2))
        q = rearrange(q, 'b (head c) h w -> b head c (h w)', head=self.num_heads)
        k = rearrange(k, 'b (head c) h w -> b head c (h w)', head=self.num_heads)
        v = rearrange(v, 'b (head c) h w -> b head c (h w)', head=self.num_heads)

        q = torch.nn.functional.normalize(q, dim=-1)
        k = torch.nn.functional.normalize(k, dim=-1)

        attn = (q @ k.transpose(-2, -1)) * self.temperature
        attn = attn.softmax(dim=-1)

        out = (attn @ v)

        out = rearrange(out, 'b head c (h w) -> b (head c) h w', head=self.num_heads, h=h, w=w)
        out_p=self.pos_emb2(v_inp)
        out = self.project_out(out)+out_p
        outfinal = out + self.bn(out1)
        return outfinal


